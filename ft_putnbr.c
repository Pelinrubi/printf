#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	num;
	int		count;

	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	count = 0;
	if (n < 0)
	{
		count += ft_putchar('-');
		n = -n;
	}
	if (n > 9)
		count += ft_putnbr(n / 10);
	num = n % 10 + '0';
	count += ft_putchar(num);
	return (count);
}
